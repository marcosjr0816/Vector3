    private void cmdCrearActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cmdCrearActionPerformed
        int longitud;
        if (txtLongitud.getText().trim().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Digite Longitud","error",JOptionPane.ERROR_MESSAGE);
            txtLongitud.requestFocusInWindow();
        }else if (Integer.parseInt(txtLongitud.getText().trim())==0){
            JOptionPane.showMessageDialog(this, "La longitud no puede ser Cero","error",JOptionPane.ERROR_MESSAGE);
            txtLongitud.requestFocusInWindow();
            txtLongitud.selectAll();
        }else{
            longitud=Integer.parseInt(txtLongitud.getText());
            v = new double [longitud];
            v1=new double [longitud];
            r = new double [longitud];
            JOptionPane.showMessageDialog(this,"Vectores creados exitosamente");

        }
    }//GEN-LAST:event_cmdCrearActionPerformed

    private void cmdLlenarManualActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cmdLlenarManualActionPerformed
        double n;
        jLabel3.setText("");
        txtResultado.setText("");
        cmdOperacion.setSelectedIndex(0);
        for (int i = 0; i < v.length; i++) {
            n=Double.parseDouble(JOptionPane.showInputDialog(this,"Digite el elemento en la posiciÃ³n " + i + " del vector 1"));
            v[i]=n;
        }
        for (int j = 0; j < v.length; j++) {
            txtResultado.append(v[j]+"\n");
        }
    }//GEN-LAST:event_cmdLlenarManualActionPerformed

    private void cmdLlenarAutoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cmdLlenarAutoActionPerformed
        double n;
        for (int i = 0; i < v.length; i++) {
            n=(int)(Math.random()* 100 + 1);
            v[i]=n;
        }
        for (int k = 0; k < v.length; k++) {
            n=(int)(Math.random()* 100 + 1);
            v1[k]=n;
        }
        jLabel3.setText("");
        txtResultado.setText("");
        cmdOperacion.setSelectedIndex(0);
        JOptionPane.showMessageDialog(this,"Vectores llenados automaticamente");
        for (int j = 0; j < v.length; j++) {
            txtResultado.append(v[j]+"\n");
        }
    }//GEN-LAST:event_cmdLlenarAutoActionPerformed

    private void cmdMostrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cmdMostrarActionPerformed
        double suma=0,resta=0;
        int op;
        op= cmdOperacion.getSelectedIndex();

        switch(op){
            case 0:
            JOptionPane.showMessageDialog(this,"Seleccione operacion","Error",JOptionPane.ERROR_MESSAGE);
            cmdOperacion.requestFocusInWindow();
            break;
            case 1:
                jLabel3.setText("suma:");
                 txtResultado.setText("");
                for (int i = 0; i < v.length; i++) {
                   r[i]=v[i]+v1[i];
                }
                for (int j = 0; j < v.length; j++) {
                txtResultado.append(r[j]+"\n");
                }
            break;
            case 2:
                
                jLabel3.setText("Resta:");
                txtResultado.setText("");
                for (int i = 0; i < v.length; i++) {
                   r[i]=v[i]-v1[i];
                }
                for (int j = 0; j < v.length; j++) {
                txtResultado.append(r[j]+"\n");
                }
            break;
        }
    }//GEN-LAST:event_cmdMostrarActionPerformed

